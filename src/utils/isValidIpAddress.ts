export function isValidIpAddress(ip: string): boolean | string {
  console.log({ip})
  const ipv4Regex = /^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/;
  const ipv6Regex = /^(?:[A-F0-9]{1,4}:){7}[A-F0-9]{1,4}$|^(?:[A-F0-9]{1,4}:){1,7}:|^(?:[A-F0-9]{1,4}:){1,6}:[A-F0-9]{1,4}$|^(?:[A-F0-9]{1,4}:){1,5}(?::[A-F0-9]{1,4}){1,2}$|^(?:[A-F0-9]{1,4}:){1,4}(?::[A-F0-9]{1,4}){1,3}$|^(?:[A-F0-9]{1,4}:){1,3}(?::[A-F0-9]{1,4}){1,4}$|^(?:[A-F0-9]{1,4}:){1,2}(?::[A-F0-9]{1,4}){1,5}$|^[A-F0-9]{1,4}:(?:(?::[A-F0-9]{1,4}){1,6})$|^(?:(?:[A-F0-9]{1,4}:){0,5}[A-F0-9]{1,4})?::(?:(?:[A-F0-9]{1,4}:){0,5}[A-F0-9]{1,4})?$|^(?:[A-F0-9]{1,4}:){1,7}:[A-F0-9]{1,4}$|^::(?:[A-F0-9]{1,4}:){1,7}$|^::1$|^fe80:(?::[A-F0-9]{0,4}){0,4}%[0-9a-zA-Z]{1,}$/;

  if (ipv4Regex.test(ip) || ipv6Regex.test(ip.toUpperCase())) {
    return true;
  }
  return "Invalid IP address format";
} 